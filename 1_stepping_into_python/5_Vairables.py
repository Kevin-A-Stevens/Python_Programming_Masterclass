"""
Variables and Types
Must begin with a letter or an underscore
They cannot begin with a number
They are case sensitive
variables are created with an equal sign (=)

Functions used:
print()
type()
"""

print("Today is a good day to learn Python")
print('Python is fun')
print("Python's strings are easy to use")
print('We can even include "quotes" in strings')
print("Hello" + " world")

# Can also store strings in a variable to be used later
greeting = "Hello"
name = "Kevin"

# if we want a space, we can add that also
print(greeting + ' ' + name)

age = 24
print(age)

# printing the type of an argument
print(type(greeting))
print(type(age))

age = "2 years"
print(age)
print(type(age))
